def normalize_lang(lang: Any) -> str:
    value = str(lang or "").strip().lower()
    if value.startswith("en"):
        return "en"
    return "ja"


def translate(key: str, lang: Any = None, default: str | None = None, **kwargs: Any) -> str:
    lang_norm = normalize_lang(lang)
    base = UI_STRINGS.get(lang_norm, {}).get(key)
    if base is None:
        base = UI_STRINGS.get("en", {}).get(key, default if default is not None else key)
    try:
        return str(base).format(**kwargs)
    except Exception:
        return str(base)


def ui_catalog(lang: Any = None) -> Dict[str, str]:
    lang_norm = normalize_lang(lang)
    return dict(UI_STRINGS.get(lang_norm, {}))


def localize_runbook_user_message(runbook: Dict[str, Any], lang: Any = None, default: str = "") -> str:
    lang_norm = normalize_lang(lang)
    i18n_block = runbook.get("user_message_template_i18n")
    if isinstance(i18n_block, dict):
        candidate = str(i18n_block.get(lang_norm) or i18n_block.get("en") or i18n_block.get("ja") or "").strip()
        if candidate:
            return candidate
    runbook_id = str(runbook.get("id") or "").strip()
    fallback_map = RUNBOOK_MESSAGES.get(runbook_id, {})
    if fallback_map:
        candidate = str(fallback_map.get(lang_norm) or fallback_map.get("en") or fallback_map.get("ja") or "").strip()
        if candidate:
            return candidate
    candidate = str(runbook.get("user_message_template") or default or "").strip()
    return candidate


def localize_runbook_title(runbook: Dict[str, Any], lang: Any = None, default: str = "") -> str:
    lang_norm = normalize_lang(lang)
    i18n_block = runbook.get("title_i18n")
    if isinstance(i18n_block, dict):
        candidate = str(i18n_block.get(lang_norm) or i18n_block.get("en") or i18n_block.get("ja") or "").strip()
        if candidate:
            return candidate
    runbook_id = str(runbook.get("id") or "").strip()
    if lang_norm == "ja":
        candidate = str(RUNBOOK_TITLES_JA.get(runbook_id) or "").strip()
        if candidate:
            return candidate
    return str(runbook.get("title") or default or "").strip()


def localize_runbook_steps(runbook: Dict[str, Any], lang: Any = None) -> List[str]:
    lang_norm = normalize_lang(lang)
    i18n_block = runbook.get("steps_i18n")
    if isinstance(i18n_block, dict):
        localized = i18n_block.get(lang_norm) or i18n_block.get("en") or i18n_block.get("ja")
        if isinstance(localized, list):
            return [str(item).strip() for item in localized if str(item).strip()]
    runbook_id = str(runbook.get("id") or "").strip()
    if lang_norm == "ja":
        localized = RUNBOOK_STEPS_JA.get(runbook_id)
        if isinstance(localized, list) and localized:
            return [str(item).strip() for item in localized if str(item).strip()]
    steps = runbook.get("steps")
    if isinstance(steps, list):
        return [str(item).strip() for item in steps if str(item).strip()]
    return []


def translate_review_text(text: str, lang: Any = None) -> str:
    lang_norm = normalize_lang(lang)
    raw = str(text or "")
    if lang_norm == "ja":
        return REVIEW_TEXT_MAP_JA.get(raw, raw)
    return raw


def translate_review_texts(items: Iterable[str], lang: Any = None) -> List[str]:
    return [translate_review_text(item, lang=lang) for item in items]
